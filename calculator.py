class Calculator:
    def Add(self,first_num,second_num):
        return first_num+second_num

    def Subtract(self,first_num,second_num):
        return first_num-second_num
    def Multiply(self,first_num,second_num):
        return first_num*second_num
    def Divide(self,first_num,second_num):
        return first_num/second_num